# math function ที่ได้ใช้บ่อยๆ

# ค่า absolute (abs)
print(abs(-10)) # 10
print(abs(5)) # 5

# ค่าสแควรูท (sqrt)
print(sqrt(4)) # 2
print(sqrt(9)) # 3

# ปัดขึ้น 1 (ceiling)
print(ceiling(5)) # 5
print(ceiling(3)) # 3
print(ceiling(-4)) # -4
print(ceiling(3.4)) # 4
print(ceiling(-9.5)) # -9

# ปัดลง 1 (floor)
print(floor(3)) # 3
print(floor(3.4)) # 3
print(floor(9.1)) # 9
print(floor(-4)) # -4
print(floor(-8.4)) # -9

# ตัดค่าทศนิยม
print(trunc(3.41)) # 3
print(trunc(54.546452134)) # 54
print(trunc(-6.6713)) # -6
print(trunc(10)) # 10

# ปัดเศษทศนิยม (round)

print(round(x = 1043432.5425, digits = 2))
print(round(x = 1043432.5425, digits = 5))
print(round(x = 1043432.5425, digits = 0))
print(round(x = -56.435, digits = 0))