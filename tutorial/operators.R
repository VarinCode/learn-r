# เครื่องหมายทางการคำนวณ (Arithmetic Operators)

# +	    บวก	            1 + 3 = 4	
# -	    ลบ	            2 - 2 = 0	
# *	    คูณ	            5 * 5 = 25	
# /	    หาร	            10 / 5 = 2	
# ^	    ยกกำลัง	        2 ^ 2 = 4	
# %%    หารเอาเศษ	    13 %% 2 = 1

# เครื่องหมายการเปรียบเทียบ (Comparison Operators)

# ==	เท่ากับ	         1 == 1 = TRUE	
# !=	ไม่เท่ากับ	      0 != 3 = TRUE	
# >	    มากกว่า	        2 > 9 = FALSE	
# <	    น้อยกว่า	     9 < 19 = TRUE	
# >=	มากกว่าเท่ากับ	  5 >= 5 = TRUE	
# <=	น้อยกว่าเท่ากับ	   3 <= 1 = TRUE

# เครื่องหมายทางตรรกศาสตร์ (Logical Operators)

# &	    และ            TRUE & TRUE = TRUE
# &&	และ            FALSE && TRUE = FALSE
# |	    หรือ            TRUE | FALSE = TRUE
# ||	หรือ            FALSE || FALSE = FALSE
# !	    ไม่             !TRUE = FALSE , !FALSE = TRUE